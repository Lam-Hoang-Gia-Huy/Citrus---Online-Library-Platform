package Review;

/**
 *
 * @author legion
 */
public class ReviewDTO {
    String owner;
    int score;
    String content;    

    public ReviewDTO() {
    }

    public ReviewDTO(String owner, int score, String content) {
        this.owner = owner;
        this.score = score;
        this.content = content;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
